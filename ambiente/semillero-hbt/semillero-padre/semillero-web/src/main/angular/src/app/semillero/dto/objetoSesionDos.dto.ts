export class ObjetoSesionDos{
    
     /**
     * Atributo que representa el id del objeto
     */
    public id: number;
     /**
     * Atributo que representa el nombre del objeto
     */
    public nombre: string;
     /**
     * Atributo que representa la editorial del objeto
     */
    public editorial: string;
     /**
     * Atributo que representa la tematica del objeto
     */
    public tematica: string;
     /**
     * Atributo que representa el numero de paginas del objeto
     */
    public numeroPaginas: number;
     /**
     * Atributo que representa el precio del objeto
     */
    public precio: number;
     /**
     * Atributo que representa los autores del objeto
     */
    public autores: string;
     /**
     * Atributo que representa si es a color del objeto
     */
    public aColor: boolean;
     /**
     * Atributo que representa la fecha de venta del objeto
     */
    public fechaVenta: Date;
    /**
     * Atributo que representa el estado del objeto
     */
    public estado: string;

}//Cierre de la clase